// Bundles the icdp Frame Agent into docs/public/demo/frame-agent.js (a single
// self-contained browser ESM the live-demo target pages load), and content-hashes
// it for cache-busting: the hash is written into each target page's <script> src
// and into frame-version.ts (which the LiveDemo component uses to version the
// iframe URLs). Because the hash is derived from the bundle, every change to
// src/frame busts every cached copy automatically — no manual version bumps.
//
// Runs as part of `docs:build` / `docs:dev`. Standalone: `node docs/scripts/build-demo-frame.mjs`.
import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { rolldown } from "rolldown";

const here = dirname(fileURLToPath(import.meta.url));
const outDir = join(here, "..", "public", "demo");
mkdirSync(outDir, { recursive: true });

const build = await rolldown({
  input: join(here, "frame-agent-entry.ts"),
  platform: "browser",
  logLevel: "silent",
});
const { output } = await build.generate({ format: "esm", minify: true });
await build.close();

const entry = output.find((chunk) => chunk.type === "chunk" && chunk.isEntry);
if (!entry || entry.type !== "chunk") throw new Error("no entry chunk produced");
const code = entry.code;

// Hash the bundle *and* the shared stylesheet so a change to either busts the
// cached copies (the page URLs and asset refs are versioned with this hash).
const css = readFileSync(join(outDir, "demo.css"), "utf8");
const hash = createHash("sha256").update(code).update(css).digest("hex").slice(0, 10);

writeFileSync(join(outDir, "frame-agent.js"), code);

// The component reads this to version the iframe page URLs with the same hash.
writeFileSync(
  join(here, "..", ".vitepress", "theme", "components", "frame-version.ts"),
  `// Generated by docs/scripts/build-demo-frame.mjs — do not edit.\nexport const FRAME_VERSION = ${JSON.stringify(hash)};\n`,
);

// Stamp the hash into every target page's frame-agent <script> src.
for (const page of ["checkout", "todo", "catalog", "dashboard", "signin"]) {
  const file = join(outDir, `${page}.html`);
  const html = readFileSync(file, "utf8")
    .replace(/frame-agent\.js(\?v=[^"'\s]*)?/g, `frame-agent.js?v=${hash}`)
    .replace(/demo\.css(\?v=[^"'\s]*)?/g, `demo.css?v=${hash}`);
  writeFileSync(file, html);
}

console.log(`wrote frame-agent.js (${(code.length / 1024).toFixed(0)} kB), version ${hash}`);
